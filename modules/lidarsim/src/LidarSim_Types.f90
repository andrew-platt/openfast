       RETURN
    END IF
    mask1 = .TRUE. 
      IF (SIZE(OutData%Weighting)>0) OutData%Weighting = UNPACK(ReKiBuf( Re_Xferred:Re_Xferred+(SIZE(OutData%Weighting))-1 ), mask1, 0.0_ReKi )
      Re_Xferred   = Re_Xferred   + SIZE(OutData%Weighting)
