  c1 = ( (t(2)-t(3))*y1%IMUOutputs + t(3)*y2%IMUOutputs - t(2)*y3%IMUOutputs ) / (t(2)*t(3)*(t(2) - t(3)))
  y_out%IMUOutputs = y1%IMUOutputs + b1 * t_out + c1 * t_out**2
  DEALLOCATE(b1)
  DEALLOCATE(c1)
END IF ! check if allocated
 END SUBROUTINE LidarSim_Output_ExtrapInterp2

