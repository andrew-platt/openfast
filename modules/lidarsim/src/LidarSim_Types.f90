IF (ALLOCATED(y_out%IMUOutputs) .AND. ALLOCATED(y1%IMUOutputs)) THEN
  ALLOCATE(b1(SIZE(y_out%IMUOutputs,1)))
  ALLOCATE(c1(SIZE(y_out%IMUOutputs,1)))
